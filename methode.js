//Fonction des carr√©s
$(".carre1").click(function () {
    let forme = $(".carre1").css("background-color");
    if(forme === "rgb(255, 0, 0)") {
        $(this).css("background-color", "white");
        $(".bloc1").css("background-color", "white");
    } else {
        $(this).css("background-color", "red");
        $(".bloc1").css("background-color", "red");
    }
})

